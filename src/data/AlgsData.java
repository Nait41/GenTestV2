package data;

public class AlgsData {
    public AlgsData() {
    }

    private String bacteria;
    private String range;
    private String rangeInterpretation;
    private String genus;
    private String description;

    public String getBacteria() {
        return bacteria;
    }

    public void setBacteria(String bacteria) {
        this.bacteria = bacteria;
    }

    public String getRange() {
        return range;
    }

    public void setRange(String range) {
        this.range = range;
    }

    public String getRangeInterpretation() {
        return rangeInterpretation;
    }

    public void setRangeInterpretation(String rangeInterpretation) {
        this.rangeInterpretation = rangeInterpretation;
    }

    public String getGenus() {
        return genus;
    }

    public void setGenus(String genus) {
        this.genus = genus;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
